CREATE TABLE PERSON
(
    ID BIGINT  PRIMARY KEY AUTO_INCREMENT,
    NAME VARCHAR(255),
    ACTIVE BOOLEAN
);

CREATE TABLE PAD
(
    ID VARCHAR(255)  PRIMARY KEY,
    PERSON VARCHAR(255)
);

CREATE TABLE NOTE
(
    ID VARCHAR(255)  PRIMARY KEY,
    PAD VARCHAR(255),
    TEXT TEXT
);

CREATE TABLE TAG
(
    ID VARCHAR(255)  PRIMARY KEY,
    PAD VARCHAR(255)
);

